/*
 * JavaScript has a built-in Math class which contains many helpful methods.
 */

// There are many already provided methods by the Math class. Some of them are mentioned below.
console.log(Math.round(3.14));
console.log(Math.pow(2, 3));
console.log(Math.ceil(1.1));
console.log(Math.floor(1.1));
console.log(Math.sqrt(2));
console.log(Math.abs(-3));
console.log(Math.sin(90));

// There are also many predefined constants in the Math class. Some of them are mentioned below.
console.log(Math.PI);
console.log(Math.SQRT2);
console.log(Math.E);
console.log(Math.LOG10E);
